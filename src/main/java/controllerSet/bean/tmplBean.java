package controllerSet.bean;

public class tmplBean {
	public int id ; 
	public String name ;
	public String owner;
	public String labelt;
	public String group; 
	public String nanyi;
	
	
	public String getLabelt() {
		return labelt;
	}
	public void setLabelt(String labelt) {
		this.labelt = labelt;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getOwner() {
		return owner;
	}
	public void setOwner(String owner) {
		this.owner = owner;
	}
	public String getGroup() {
		return group;
	}
	public void setGroup(String group) {
		this.group = group;
	}
	public String getNanyi() {
		return nanyi;
	}
	public void setNanyi(String nanyi) {
		this.nanyi = nanyi;
	}
	
}
